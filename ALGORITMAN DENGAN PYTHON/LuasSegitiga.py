# MENGHITUNG LUAS SEGITIGA
# I.S : Pengguna Memasukkan Alas Segitiga (A) dan Tinggi segitiga (t) 
# F.S : Menampilkan hasil perhitungan luas segitiga

alasSegitiga = int(input('Masukkan Alas Segitiga :'))
tinggiSegitiga = int(input('Masukkan Tinggi Segitiga :'))
HasilLuasSegitiga = int(0.5 * alasSegitiga * tinggiSegitiga)

# Tampilkan Hasil Luas Segitiga

print("hasil Luas Segitiga dari alas segitiga", alasSegitiga, 'dan tinggi segitiga', tinggiSegitiga, 'adalah', HasilLuasSegitiga)


